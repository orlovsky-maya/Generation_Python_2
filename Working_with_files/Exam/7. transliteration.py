dict = {
    'а': 'a', 'к': 'k', 'х': 'h', 'б': 'b', 'л': 'l', 'ц': 'c', 'в': 'v', 'м': 'm', 'ч': 'ch',
    'г': 'g', 'н': 'n', 'ш': 'sh', 'д': 'd', 'о': 'o', 'щ': 'shh', 'е': 'e', 'п': 'p', 'ъ': '*',
    'ё': 'jo', 'р': 'r', 'ы': 'y', 'ж': 'zh', 'с': 's', 'ь': "'", 'з': 'z', 'т': 't', 'э': 'je',
    'и': 'i', 'у': 'u', 'ю': 'ju', 'й': 'j', 'ф': 'f', 'я': 'ya'
    }
with open('cyrillic.txt') as cyr_file, open('transliteration.txt', 'w', encoding='utf-8') as result_file:

    cyr_file = cyr_file.read()
    result = ''
    for c in cyr_file:
        if c.lower() in dict:
            if c.isalpha():
                if c.isupper():
                    result += (dict[c.lower()].capitalize())
                else:
                    result += (dict[c])
        else:
            result += c
    result_file.write(result)

